package com.clickhealth.object;

public class Columna {
	private String numUno;
	private String numDos;
	private String numTres;
	private String numCuatro;
	private String numCinco;
	private String numSeis;
	private String numSiete;
	
	
	
	public Columna() {
		super();
	}
	public String getNumUno() {
		return numUno;
	}
	public void setNumUno(String numUno) {
		this.numUno = numUno;
	}
	public String getNumDos() {
		return numDos;
	}
	public void setNumDos(String numDos) {
		this.numDos = numDos;
	}
	public String getNumTres() {
		return numTres;
	}
	public void setNumTres(String numTres) {
		this.numTres = numTres;
	}
	public String getNumCuatro() {
		return numCuatro;
	}
	public void setNumCuatro(String numCuatro) {
		this.numCuatro = numCuatro;
	}
	public String getNumCinco() {
		return numCinco;
	}
	public void setNumCinco(String numCinco) {
		this.numCinco = numCinco;
	}
	public String getNumSeis() {
		return numSeis;
	}
	public void setNumSeis(String numSeis) {
		this.numSeis = numSeis;
	}
	public String getNumSiete() {
		return numSiete;
	}
	public void setNumSiete(String numSiete) {
		this.numSiete = numSiete;
	}
	
	
	
}
